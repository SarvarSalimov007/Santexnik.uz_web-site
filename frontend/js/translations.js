// ===== Translation Helper Function =====
function t(key) {
    const lang = getCurrentLanguage();
    if (TRANSLATIONS[key] && TRANSLATIONS[key][lang]) {
        return TRANSLATIONS[key][lang];
    }
    // Fallback to Uzbek Latin
    if (TRANSLATIONS[key] && TRANSLATIONS[key].uz) {
        return TRANSLATIONS[key].uz;
    }
    return key;
}

function getCurrentLanguage() {
    return localStorage.getItem('santexnik-lang') || 'uz';
}

function setLanguage(lang) {
    if (!LANGUAGES[lang]) return;
    localStorage.setItem('santexnik-lang', lang);
    document.documentElement.setAttribute('lang', lang === 'uz_cyrl' ? 'uz' : lang);
    applyTranslations();
    updateLanguageSwitcherUI();
}

function applyTranslations() {
    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        if (translation) {
            el.textContent = translation;
        }
    });

    // Translate elements with data-i18n-html (for innerHTML)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        const translation = t(key);
        if (translation) {
            el.innerHTML = translation;
        }
    });

    // Translate placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const translation = t(key);
        if (translation) {
            el.setAttribute('placeholder', translation);
        }
    });

    // Translate titles
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        const translation = t(key);
        if (translation) {
            el.setAttribute('title', translation);
        }
    });

    // Update page title
    const lang = getCurrentLanguage();
    const titles = {
        uz: "My santexnik — O'zbekistondagi eng ishonchli ustalar platformasi",
        uz_cyrl: "My santexnik — Ўзбекистондаги энг ишончли усталар платформаси",
        en: "My santexnik — The most trusted workers platform in Uzbekistan",
        ru: "My santexnik — Самая надёжная платформа мастеров в Узбекистане",
        tj: "My santexnik — Платформаи боэътимодтарини устоҳо дар Ӯзбекистон"
    };
    document.title = titles[lang] || titles.uz;

    // Update CATEGORY_NAMES for dynamic content
    updateCategoryNames();
    
    // Update theme toggle tooltips to match new language
    if (typeof applyTheme === 'function') {
        applyTheme(document.documentElement.getAttribute('data-theme') || 'light');
    }
}

