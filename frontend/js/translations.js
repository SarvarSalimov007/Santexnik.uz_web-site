function updateLanguageSwitcherUI() {
    const currentLang = getCurrentLanguage();
    
    // Update active state on all language buttons
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === currentLang);
    });

    // Update current language display
    document.querySelectorAll('.current-lang-flag').forEach(el => {
        const flagKey = LANGUAGES[currentLang].flag;
        el.innerHTML = FLAG_SVGS[flagKey] || '';
    });
    document.querySelectorAll('.current-lang-name').forEach(el => {
        el.textContent = LANGUAGES[currentLang].nativeName;
    });
}

function createLanguageSwitcher() {
    const currentLang = getCurrentLanguage();
    const flagKey = LANGUAGES[currentLang].flag;
    
    const html = `
        <div class="lang-switcher" id="langSwitcher">
            <button class="lang-current" id="langToggle" aria-label="Change language">
                <span class="current-lang-flag">${FLAG_SVGS[flagKey]}</span>
                <span class="current-lang-name">${LANGUAGES[currentLang].nativeName}</span>
                <i class="fa-solid fa-chevron-down lang-arrow"></i>
            </button>
            <div class="lang-dropdown" id="langDropdown">
                ${Object.entries(LANGUAGES).map(([code, lang]) => `
                    <button class="lang-option ${code === currentLang ? 'active' : ''}" data-lang="${code}">
                        <span class="lang-option-flag">${FLAG_SVGS[lang.flag]}</span>
                        <span class="lang-option-name">${lang.nativeName}</span>
                        ${code === currentLang ? '<i class="fa-solid fa-check lang-check"></i>' : ''}
                    </button>
                `).join('')}
            </div>
        </div>
    `;
    return html;
}

function initLanguageSwitcher() {
    // Insert language switcher into navbar
    const navActions = document.querySelector('.nav-actions');
    if (navActions) {
        const langDiv = document.createElement('div');
        langDiv.style.display = 'contents';
        langDiv.innerHTML = createLanguageSwitcher();
        navActions.insertBefore(langDiv, navActions.firstChild);
    }

    // Insert language switcher into mobile menu
    const mobileMenu = document.getElementById('mobileMenu');
    if (mobileMenu) {
        const mobileLangDiv = document.createElement('div');
        mobileLangDiv.className = 'mobile-lang-switcher';
        mobileLangDiv.innerHTML = `
            <div class="mobile-lang-title" data-i18n="nav_language"><i class="fa-solid fa-globe"></i> ${t('nav_language')}</div>
            <div class="mobile-lang-options">
                ${Object.entries(LANGUAGES).map(([code, lang]) => `
                    <button class="mobile-lang-btn lang-option ${code === getCurrentLanguage() ? 'active' : ''}" data-lang="${code}">
                        <span class="lang-option-flag">${FLAG_SVGS[lang.flag]}</span>
                        <span>${lang.nativeName}</span>
                    </button>
                `).join('')}
            </div>
        `;
        // Insert before theme buttons
        const themeDiv = mobileMenu.querySelector('div[style*="display: flex"]');
        if (themeDiv) {
            mobileMenu.insertBefore(mobileLangDiv, themeDiv);
        } else {
            mobileMenu.appendChild(mobileLangDiv);
        }
    }

    // Desktop dropdown toggle
    const langToggle = document.getElementById('langToggle');
    const langDropdown = document.getElementById('langDropdown');
    
    if (langToggle && langDropdown) {
        langToggle.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('open');
            langToggle.classList.toggle('open');
        });
        
        // Close dropdown on outside click
        document.addEventListener('click', (e) => {
            if (!e.target.closest('.lang-switcher')) {
                langDropdown.classList.remove('open');
                langToggle.classList.remove('open');
            }
        });
    }

    // Language selection handlers
    document.querySelectorAll('.lang-option').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            setLanguage(lang);
            
            // Close dropdown
            if (langDropdown) {
                langDropdown.classList.remove('open');
                if (langToggle) langToggle.classList.remove('open');
            }
            
            // Re-render workers with new language
            if (typeof renderWorkers === 'function') {
                renderWorkers();
            }

            // Update dropdown checkmarks
            document.querySelectorAll('.lang-option').forEach(b => {
                b.classList.toggle('active', b.dataset.lang === lang);
                const check = b.querySelector('.lang-check');
                if (check) check.remove();
                if (b.dataset.lang === lang) {
                    const icon = document.createElement('i');
                    icon.className = 'fa-solid fa-check lang-check';
                    b.appendChild(icon);
                }
            });
        });
    });

    // Apply saved language on init
    applyTranslations();
    updateLanguageSwitcherUI();
}
